.DATA
	A:.WORD 15
	O:.asciz "ODD NUMBER OF 1S"
	E:.asciz "EVEN NUMBER OF 1S"
.TEXT
	LDR R1,=A
	LDR R2,[R1]
	MOV R6,#0;NUMBER OF 1S
	MOV R9,#0
	MOV R7,#0;COUNTER
	L:	ADD R7,R7,#1
		AND R3,R2,#1
		CMP R3,#1;FIRST BIT IS 1
		ADDEQ R6,R6,#1
		ADD R2,R9,R2,LSR #1
		CMP R7,#32;JUMP
		BNE L
	AND R9,R6,#1
	CMP R9,#1
	BEQ ODD
	LDR R0,=E
	SWI 0X02
	SWI 0X011
	ODD:	LDR R0,=O
		SWI 0X02