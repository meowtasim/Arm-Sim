.DATA
	A:.WORD 0,1,2,3,-1,5
.TEXT
	LDR R1,=A
	MOV R4,#6;KEY LENGTH
	MOV R5,R4;counter
	MOV R6,#0;zero
	MOV R7,#0;positive
	MOV R8,#0;negative
	L:	LDR R2,[R1];VALUE OF NUMBERS
		CMP R2,#0
		ADDEQ R6,R6,#1;INCREMENT ZERO COUNTER
		ADDGT R7,R7,#1;INCREMENT POSITIVE COUNTER
		ADDLT R8,R8,#1;INCREMENT NEGATIVE COUNTER
		SUB R5,R5,#1
		ADD R1,R1,#4
		CMP R5,#0
		BNE L